var jml_Mobil = 10;
var mbl_Beroperasi = 6;
var no_Mobil = 1;


for (no_Mobil = 1; no_Mobil <= jml_Mobil; no_Mobil++) {

    if (no_Mobil <= mbl_Beroperasi) {
        console.log('Mobil No. ' + no_Mobil + ' beroperasi dengan baik');
    } else {
        console.log('Mobil No. ' + no_Mobil + '  dengan tidak beroperasi')
    }

}